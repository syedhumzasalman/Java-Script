// QUESTION 03  Do the following using JS Mathematic Expressions

function question03() {

    // a. Declare a variable
    var number;


    // b. Show the value of variable in the browser
    document.write("Value after variable declaration is: " + number + "<br><br>");


    // c. Initialize the variable with some number
    number = 5;


    // d. Show the initial value of the variable
    document.write("Initial value: " + number + "<br> <br>");


    // e. Increment the variable
    number = number + 1;

    // f. Show the value after increment
    document.write("Value after increment is: " + number + "<br><br>");

    // g. Add 7 to the variable
    number = number + 7;

    // h. Show the value after addition
    document.write("Value after addition is: " + number + "<br><br>");

    // i. Decrement the variable
    number = number - 1;

    // j. Show the value after decrement
    document.write("Value after decrement is: " + number + "<br><br>");

    // k. Show the remainder after dividing the variable by 3
    var remainder = number % 3;

    // l. Output the remainder
    document.write("The remainder is: " + remainder);


}





